// Function to get a random adjective
export function getRandomAdjective(): string {
  return composureAdjectives[Math.floor(Math.random() * composureAdjectives.length)];
}

// Function to get adjective based on composure score (0-100)
export function getComposureAdjective(score: number): string {
  if (score >= 90) {
    // Excellent - top tier positive adjectives
    const excellent = composureAdjectives.slice(0, 50);
    return excellent[Math.floor(Math.random() * excellent.length)];
  } else if (score >= 75) {
    // Good - positive and engaged adjectives
    const good = composureAdjectives.slice(50, 150);
    return good[Math.floor(Math.random() * good.length)];
  } else if (score >= 60) {
    // Fair - neutral and moderate adjectives
    const fair = composureAdjectives.slice(150, 250);
    return fair[Math.floor(Math.random() * fair.length)];
  } else if (score >= 40) {
    // Needs improvement - nervous and uncertain
    const needsImprovement = composureAdjectives.slice(250, 350);
    return needsImprovement[Math.floor(Math.random() * needsImprovement.length)];
  } else {
    // Poor - closed and defensive
    const poor = composureAdjectives.slice(350, 450);
    return poor[Math.floor(Math.random() * poor.length)];
  }
}

// Function to get multiple adjectives based on specific metrics
export function getMetricAdjectives(metrics: { label: string; value: number }[]): string {
  const avgScore = metrics.reduce((sum, m) => sum + m.value, 0) / metrics.length;
  return getComposureAdjective(avgScore);
}
